public class variables {
    public static void main(String[] args){
        // Short for school size
        short SchoolSize = 500;
        System.out.println(SchoolSize);
        //  Long for Size jupiters mass
        long Jmass = 500000000;
        System.out.println(Jmass);
        // Char for Initial
        char Initial = 'M';
        System.out.println(Initial);
        // bool for yes or no question
        boolean yesNo = true;
        System.out.println(yesNo);
        // byte for persons age
        byte age = 97;
        System.out.println(age);
        // Int for Country Pop
        int Pop = 3200;
        System.out.println(Pop);
    }
}